def is_valid_duration(duration: int) -> bool:
    return 0 < duration <= MAX_DURATION


def is_library_music_url(audio_url: str) -> bool:
    if not audio_url: return False
    for domain in TIKTOK_MUSIC_CDN_DOMAINS:
        if domain in audio_url: return True
    return False


def has_known_copyright_pattern(audio_name: str) -> bool:
    name_lower = audio_name.strip().lower()
    for pattern in KNOWN_COPYRIGHT_PATTERNS:
        if re.search(pattern, name_lower, re.IGNORECASE): return True
    return False


def is_from_known_copyright_creator(video_url: str) -> bool:
    if not video_url: return False
    url_lower = video_url.lower()
    for creator in KNOWN_COPYRIGHT_CREATORS:
        if creator.lower() in url_lower: return True
    return False


def is_blacklisted(audio_name: str) -> bool:
    name_lower = audio_name.lower()
    for word in BLACKLIST_KEYWORDS:
        if re.search(r'\b' + re.escape(word) + r'\b', name_lower) or word in name_lower:
            return True
    return False


def is_original_sound(audio_name: str) -> bool:
    name_lower = audio_name.strip().lower()
    for kw in SOFT_REJECT_KEYWORDS:
        if kw in name_lower: return False
    original_patterns = [
        "âm thanh gốc", "original sound", "originalton",
        "suara asli", "오리지널 사운드", "sonido original",
        "som original", "son original", "เสียงต้นฉบับ",
        "original audio", "my voice", "my sound",
    ]
    for p in original_patterns:
        if p in name_lower: return True
    for kw in SOFT_ACCEPT_KEYWORDS:
        if kw in name_lower: return True
    if len(name_lower) < 60 and not any(bw in name_lower for bw in ["song", "music", "track", "album"]):
        return True
    return False


