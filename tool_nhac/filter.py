def compute_quality_score(audio: AudioMetadata) -> float:
    """IDEA 6: Tính điểm chất lượng đa chiều (0.0 - 1.0).
    
    Công thức:
      - speech_ratio     : 40 điểm (tỉ lệ giọng nói)
      - usage_count      : 30 điểm (LSD, chuẩn hoá max 50K)
      - recency          : 20 điểm (video càng gần đây càng cao)
      - original_sound   : 10 điểm (bonus nếu tên có "original sound")
    """
    score = 0.0

    # 1. Speech ratio (40%)
    score += min(audio.speech_ratio, 1.0) * 40.0

    # 2. Usage count (30%) - chuẩn hoá tối đa 50K LSD
    usage_norm = min(max(audio.usage_count, 0) / 50000, 1.0)
    score += usage_norm * 30.0

    # 3. Recency (20%) - năm 2026 = 20đ, năm 2023 = 5đ
    if audio.create_time > 0:
        year = datetime.fromtimestamp(audio.create_time).year
        recency_map = {2026: 20, 2025: 16, 2024: 12, 2023: 5}
        score += recency_map.get(year, 0)
    
    # 4. Original sound bonus (10%)
    name_lower = audio.audio_name.lower()
    if any(p in name_lower for p in ["original sound", "âm thanh gốc", "originalton", "suara asli"]):
        score += 10.0

    # Chuẩn hoá về 0.0 - 1.0
    return round(min(score / 100.0, 1.0), 3)


