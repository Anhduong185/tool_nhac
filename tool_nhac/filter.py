def is_valid_audio(audio: AudioMetadata) -> tuple[bool, str]:
    """Pipeline lọc đồng bộ - chạy nhanh, không cần await.
    
    Thứ tự kiểm tra:
    1. Validate kiểu dữ liệu cơ bản
    2. Thời lượng hợp lệ (<=59s)
    3. Kiểm tra hiệu ứng âm thanh (IDEA 2)
    4. Kiểm tra nhạc nền qua tên (IDEA 2)
    5. Media type classifier (IDEA 4)
    6. URL không phải CDN nhạc TikTok
    7. Phải là tiếng gốc creator
    8. Không khớp pattern nhạc bản quyền
    9. Không từ creator chuyên nhạc
    10. Không có từ khoá blacklist
    11. Kiểm tra năm + threshold LSD động (IDEA 1)
    """
    if not isinstance(audio.duration, (int, float)) or not isinstance(audio.usage_count, (int, float)):
        return False, "Invalid metadata types"

    if audio.duration <= 0:
        return False, "Duration is zero or negative"

    # 2. Thời lượng
    if not is_valid_duration(audio.duration):
        return False, f"Duration > {MAX_DURATION}s"

    # 3. IDEA 2: Phát hiện hiệu ứng âm thanh (tiếng cười, SFX)
    if has_sound_effect(audio.audio_name):
        return False, "Sound effect detected (tiếng cười / SFX)"

    # 4. IDEA 2: Phát hiện nhạc nền qua tên
    if has_background_music_name(audio.audio_name):
        return False, "Background music name detected"

    # 5. IDEA 4: Phân loại loại nội dung phim/TV cấm
    allowed, media_reason = is_allowed_media_type(audio.audio_name, audio.author_username)
    if not allowed:
        return False, media_reason

    # 6. URL CDN nhạc TikTok
    if is_library_music_url(audio.audio_url):
        return False, "Library music URL (TikTok CDN)"

    # 7. Phải là original sound
    if not is_original_sound(audio.audio_name):
        return False, "Not an original sound"

    # 8. Pattern nhạc bản quyền
    if has_known_copyright_pattern(audio.audio_name):
        return False, "Matches known copyright pattern"

    # 9. Creator chuyên nhạc bản quyền
    if is_from_known_copyright_creator(audio.video_url):
        return False, "From known copyright creator"

    # 10. Blacklist keyword
    if is_blacklisted(audio.audio_name):
        return False, "Blacklisted keyword in name"

    # 11. IDEA 1+3: Kiểm tra năm và ngưỡng LSD động
    usage = audio.usage_count if audio.usage_count != -1 else 0
    create_time = audio.create_time

    # Kiểm tra video trước 2023
    if create_time > 0 and create_time < 1672531200:  # 2023-01-01
        # IDEA 3: Re-use exception - video cũ nhưng usage cao bất thường
        if is_recent_reuse(create_time, usage):
            logger.debug(f"Re-use detected: {audio.audio_name} ({usage:,} LSD)")
            # Cho qua nhưng đặt usage_count để check_usage xác nhận lại
        else:
            return False, "Video created before 2023 (no reuse signal)"

    # Áp dụng ngưỡng LSD động (chỉ reject nếu tool đã biết usage)
    if usage != 0:  # usage=0 hoặc -1 → chưa biết → để Phase 2 check
        min_required = get_dynamic_min_usage(create_time)
        if usage < min_required:
            year = datetime.fromtimestamp(create_time).year if create_time > 0 else "?"
            return False, f"LSD {usage:,} < {min_required:,} (yêu cầu cho năm {year})"

    return True, "Valid"


async def is_valid_audio_async(audio: AudioMetadata) -> tuple[bool, str]:
    """Bản async của pipeline lọc - có thêm kiểm tra tác giả từ DB/Sheet.
    Gọi hàm này nếu cần check đầy đủ bao gồm DB lookup.
    """
    # Chạy pipeline đồng bộ trước
    ok, reason = is_valid_audio(audio)
    if not ok:
        return False, reason

    # Bổ sung: Lọc tác giả đã biết (2 tuần)
    from database import is_author_known
    if await is_author_known(audio.author_username):
        two_weeks_ago = int(time.time()) - (14 * 24 * 60 * 60)
        if audio.create_time > 0 and audio.create_time < two_weeks_ago:
            return False, "Known author, video older than 2 weeks"

    return True, "Valid"
