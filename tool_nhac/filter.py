def is_allowed_media_type(audio_name: str, author_username: str = "") -> tuple[bool, str]:
    """IDEA 4: Phân loại loại nội dung.
    
    Returns:
        (is_allowed, reason)
    """
    name_lower = audio_name.strip().lower()
    author_lower = author_username.lower()

    # Hard block: phim/TV chuyên nghiệp có logo
    for kw in FILM_TV_BLOCK_KEYWORDS:
        if kw in name_lower:
            return False, f"Blocked media type: film/TV ({kw})"

    # Hard block: kênh chuyên nhạc nền
    blocked_channel_patterns = [
        r"music\s*official", r"^music\d*$", r"beatstore",
        r"soundcloud", r"nhac\s*tru[oa]ng",
    ]
    for pat in blocked_channel_patterns:
        if re.search(pat, author_lower):
            return False, f"Channel blocked: music-only channel"

    return True, "OK"


