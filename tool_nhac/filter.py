# IDEA 1: SMART LSD THRESHOLD ENGINE - Ngưỡng LSD động theo năm + tuần
# =====================================================================
ONE_WEEK_SEC = 7 * 24 * 3600

def get_dynamic_min_usage(create_time: int) -> int:
    """Tính ngưỡng LSD tối thiểu dựa theo năm đăng và thời gian gần đây.
    
    Spec:
      - Trong 1 tuần qua  → 300 LSD
      - 2026 (bình thường) → 500 LSD
      - 2025               → 500 LSD
      - 2024               → 500 LSD
      - 2023               → 1000 LSD
      - < 2023             → LOẠI (trừ khi được dùng lại gần đây - xử lý riêng)
    """
    if create_time <= 0:
        return 500  # Không có timestamp → dùng ngưỡng mặc định

    now = time.time()
    if create_time >= now - ONE_WEEK_SEC:
        return 300  # Video trong 1 tuần → threshold thấp nhất

    year = datetime.fromtimestamp(create_time).year
    if year >= 2025: return 500
    if year == 2024: return 500
    if year == 2023: return 1000
    return 999999  # Trước 2023 → không lấy (trừ re-use)


def is_recent_reuse(create_time: int, usage_count: int) -> bool:
    """IDEA 3: Phát hiện audio cũ được dùng lại nhiều gần đây.
    
    Logic: Nếu video cũ (trước 2023) nhưng usage_count rất cao so với tuổi
    thì có thể đang được viral trở lại.
    Ngưỡng: usage_count > 5000 để bù lại tuổi già.
    """
    if create_time <= 0 or usage_count <= 0:
        return False

    year = datetime.fromtimestamp(create_time).year
    if year >= 2023:
        return False  # Không phải "cũ"

    # Video trước 2023, nhưng usage > 5000 → khả năng cao đang được re-use
    return usage_count >= 5000


